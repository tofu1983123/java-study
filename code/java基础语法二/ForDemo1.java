class ForDemo1 
{
	public static void main(String[] args) 
	{
		System.out.println("Start");
		for (int i = 1;i <= 10 ;i++)
		{
			System.out.println("Hello World!");
		}
		System.out.println("Bye-Bye");
		System.out.println("==============");
		for (int i = 100;i > 90 ;i--)
		{
			System.out.println("Hello World!");
		}

	}
}
