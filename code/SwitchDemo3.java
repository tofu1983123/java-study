class SwitchDemo3 
{
	public static void main(String[] args) 
	{
		int a = 5;
		switch (a)
		{
		default:
			System.out.println("dd");
		case 1:
			System.out.println("aa");
		case 2:
			System.out.println("bb");
		case 3:
			System.out.println("cc");
		//default:
			//System.out.println("dd");
		}
	}
}
